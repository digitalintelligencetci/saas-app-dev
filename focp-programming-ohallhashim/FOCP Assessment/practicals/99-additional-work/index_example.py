course = "Python for Beginners"
print(course[0])    # Python counts from 0 / 0 returns the first character

course = "Python for Beginners"
print(course[-6])   # -1 returns the sixth character

course = "Python for Beginners"
print(course[1:-1]) # (slicing the string) / 1 is the second character and -1 is the last character
